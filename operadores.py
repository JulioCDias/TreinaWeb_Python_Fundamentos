"""
Operadores em Python:
- Aritméticos: +, -, *, /, //, %, **
- Comparação: ==, !=, >, <, >=, <=
- Lógicos: and, or, not
- Atribuição: =, +=, -=, *=, /=, //=, %=, **=
- Identidade: is, is not
- Associação: in, not in
"""

a = 10
b = 5
print(a + b)  # Soma
print(a - b)  # Subtração
print(a * b)  # Multiplicação
print(a / b)  # Divisão
print(a // b)  # Divisão inteira
print(a % b)  # Módulo
print(a**b)  # Exponenciação
print(a == b)  # Igualdade
print(a != b)  # Diferença
print(a > b)  # Maior que
print(a < b)  # Menor que
print(a >= b)  # Maior ou igual a
print(a <= b)  # Menor ou igual a
print(a > 0 and b > 0)  # Lógico AND
print(a > 0 or b > 0)  # Lógico OR
print(not (a > 0))  # Lógico NOT
print(a is b)  # Identidade
print(a is not b)  # Não é o mesmo objeto
print(a in [1, 2, 3, 10])  # Associação
print(b not in [1, 2, 3, 10])  # Não está na lista
